    <h2>Society Features</h2>

    <h3>Habitation</h3>

    People follow the shifting river flows and natural food resources,
    setting up semipermanent housing in commonly used areas. At this
    point, settlements aren't really settlements, but rather local groups
    of clans.

    <h3>Ideas</h3>

    People have many ideas but a few that are particularly common and
    important are:

    <ul>
        <li>The world is full of powerful beings, including natural
            forces and our ancestors.
        </li>
        <li>Someday we'll live in big flourishing towns like the
            big nations of the north!
        </li>
    </ul>

    <h3>Roles and Status</h3>

    <p>There are no formally defined social roles that people always fill 
    other than family relationships such as parent-child. However, during
    ritual season there may be more defined roles.</p>
    
    <p>There is also some differentiation of roles based on age and gender.</p>

    <p>Clans have relative prestige based on their ritual knowledge,
       foraging and tending knowledge, and size.
    </p>

    <h3>Ritual Season</h3>

    <p>Each year, there is a ritual season, where the people work together to 
    both produce food and put on big rituals and celebrations. People work more
    effectively and are happier during this time &mdash; if the rituals go well.</p>

    <p>During ritual season, a few people may occupy defined leadership roles, to
    guide the work and the rituals. Rituals are more effective when leaders have
    more ritual knowledge.</p>

    <h3>Subsistence</h3>

    <p>{society.irrigation}</p>

    <p>There is no land ownership: ritual leaders, with community assent,
       direct work to tend and harvest plants and the sharing out of the
       harvest.
    </p>

    <h3>Peace</h3>

    <p>The people of this region are spread out, with plenty of resources and 
    a common goal of using them, so there is little organized conflict &mdash;
    for now.
    </p>
